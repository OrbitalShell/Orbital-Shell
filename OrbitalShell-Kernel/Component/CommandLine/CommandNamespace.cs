namespace OrbitalShell.Component.CommandLine
{
    /// <summary>
    /// kernel commands namespaces
    /// </summary>
    public enum CommandNamespace
    {
        /// <summary>
        /// shell kernel
        /// </summary>
        kernel,

        /// <summary>
        /// system
        /// </summary>
        sys,

        /// <summary>
        /// filesystem
        /// </summary>
        fs,

        /// <summary>
        /// text file
        /// </summary>
        tf,

        /// <summary>
        /// text
        /// </summary>
        text,

        /// <summary>
        /// console
        /// </summary>
        cons,

        /// <summary>
        /// shell (kernel)
        /// </summary>
        shell,

        /// <summary>
        /// shell-var : variables
        /// </summary>
        var,

        /// <summary>
        /// shell-app : application
        /// </summary>
        app,

        /// <summary>
        /// shell-history : history
        /// </summary>
        history,

        /// <summary>
        /// shell-module : module
        /// </summary>
        module,

        /// <summary>
        /// shell-help : help
        /// </summary>
        help,

        /// <summary>
        /// test (miscelaneous)
        /// </summary>
        test,

        /// <summary>
        /// development usefull (miscelaneous)
        /// </summary>
        dev,

        /// <summary>
        /// place for typed data tools (eg. csv)
        /// </summary>
        data,

        /// <summary>
        /// might be possible to find games
        /// </summary>
        games
    }
}