namespace OrbitalShell.Component.Shell.Variable
{
    /// <summary>
    /// standard shell environment namespaces
    /// </summary>
    public enum ShellEnvironmentNamespace
    {
        /// <summary>
        /// os environment (underlying shell/os)
        /// </summary>
        os,

        /// <summary>
        /// shell settings
        /// </summary>
        settings,

        /// <summary>
        /// commands settings
        /// </summary>        
        com,

        /// <summary>
        /// debug settings
        /// </summary>
        debug,

        /// <summary>
        /// display settings
        /// </summary>
        display,

    }
}
